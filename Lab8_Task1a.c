#include <stdio.h>
#include <stdlib.h>
#include <locale.h>
#include <math.h>

int main() {
	setlocale(LC_ALL, "RUS");
	int n;
	double m;
	double s = 2.;
	double z = 2.;

	puts("Введите число:\n");
	scanf("%i", &n);

	for (int i = 1; i < n; i++) {
		s *= z;
	}

	m = s / 1024.;

	printf("\t\t\t\t\t\t Результат: %.1lfK |", m);

}
